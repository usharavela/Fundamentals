package github;

public class Pattern {
       public static void main(String args[])
{
    	   int rows=5,i,j;
    	   for(i=1 ; i<=rows ; i++)
    	   {
    		   for(j=1 ; j<=i ;j++)
    		   {
    			   System.out.print("* ");
    		   }
    		   System.out.print('\n');
    	   }
    	   
}
}
